package com.pankaj.pkg;

public class Alien {
	
	private int alienId;
	private String alienName;
	private String alienLanguage;
	
	public void setAlienId(int alienId) {
		this.alienId = alienId;
	}
	public int getAlienId() {
		return alienId;
	}
	
	public void setAlienName(String alienName) {
		this.alienName = alienName;
	}
	public String getAlienName() {
		return alienName;
	}
	
	public void setAlienLanguage(String alienLanguage) {
		this.alienLanguage = alienLanguage;
	}
	public String getAlienLanguage() {
		return alienLanguage;
	}
	
}
